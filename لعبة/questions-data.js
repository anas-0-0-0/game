function parseQuestionsFromRawText(rawText, { defaultCategory = "علمي", maxOptionsPerQuestion = 4 } = {}) {
  const text = (rawText || "").replace(/\r/g, "");
  const lines = text
    .split("\n")
    .map((l) => l.trim())
    .filter((l) => l.length > 0);

  /** @type {{question:string, options:string[], answer:number, category:string}[]} */
  const questions = [];
  let current = null;

  const startNewQuestion = (questionLine) => {
    if (current && current.options.length >= 4 && current.answer >= 0) {
      questions.push(current);
    }
    current = {
      question: questionLine,
      options: [],
      answer: -1,
      category: defaultCategory,
    };
  };

  for (const line of lines) {
    const qMatch = line.match(/^\d+\.\s*(.+)$/);
    if (qMatch) {
      startNewQuestion(qMatch[1].trim());
      continue;
    }

    if (!current) continue;
    if (current.options.length >= maxOptionsPerQuestion) continue; // تجاهل أي سطور زيادة

    const isCorrect = line.includes("(صح)") || line.includes("(✓)") || line.includes("✓");
    const clean = line
      .replace(/\(صح\)/g, "")
      .replace(/\(✓\)/g, "")
      .replace(/✓/g, "")
      .trim();

    if (!clean) continue;

    current.options.push(clean);
    if (isCorrect && current.answer < 0) {
      current.answer = current.options.length - 1;
    }
  }

  if (current && current.options.length >= 2) {
    // لو ما تم تحديد إجابة صحيحة بعلامة (صح)، اعتبر أول خيار هو الصحيح
    if (current.answer < 0) {
      current.answer = 0;
    }
    questions.push(current);
  }

  return questions;
}

